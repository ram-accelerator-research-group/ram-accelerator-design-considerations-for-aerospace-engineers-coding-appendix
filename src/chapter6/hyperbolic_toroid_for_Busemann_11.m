max_z_value_of_hyperbolic_toroid,min_z_value_of_hyperbolic_toroid, ...
max_length_of_z_axis_aligned_square_inside_hyperbolic_toroid, ...
max_square_area,inner_radial_extent_of_max_square,outer_radial_extent_of_max_squar e, ...
