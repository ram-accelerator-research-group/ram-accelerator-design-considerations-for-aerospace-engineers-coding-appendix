% Excise the first row of each "upper half" meshgrid, since this is a set
% of redundant points, along the innermost radius of the toroid, that is
% shared in common with the lower half of the toroid.

[row_num,~] = size(x_uh_inner_outer_mat);

x_uh_inner_outer_mat = x_uh_inner_outer_mat(2:1:row_num,:) ;

y_uh_inner_outer_mat = y_uh_inner_outer_mat(2:1:row_num,:) ;

z_uh_inner_outer_mat = z_uh_inner_outer_mat(2:1:row_num,:) ;

% Reverse the order of the meshgrids for the upper half of the hyperbolic
% toroid. This ensures that, when the two sets of meshgrids are merged, the
% running order of the points will proceed with a counterclockwise
% rotation (i.e. outer-upper-inner-lower-outer), when viewed from the +x
% section of the longitudinally intersecting xz-plane.
%
% NOTE: The radially outermost ring of points on the hyperbolic toroid is
% also duplicated between the upper and lower half meshgrids of the torus.
% However, the corresponding duplicate rows cannot be deleted without
% splitting open the surface of the toroid.

x_uh_inner_outer_mat = flipud(x_uh_inner_outer_mat) ;

y_uh_inner_outer_mat = flipud(y_uh_inner_outer_mat) ;

z_uh_inner_outer_mat = flipud(z_uh_inner_outer_mat) ;

x_hyperbolic_toroid_mat = vertcat(x_uh_inner_outer_mat,x_lh_inner_outer_mat);

y_hyperbolic_toroid_mat = vertcat(y_uh_inner_outer_mat,y_lh_inner_outer_mat);

z_hyperbolic_toroid_mat = vertcat(z_uh_inner_outer_mat,z_lh_inner_outer_mat);

