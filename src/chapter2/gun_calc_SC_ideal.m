legend(label1,label2, 'Location' ,'northeast' ,'FontSize' ,18,'FontWeight' ,'bold')
else
end

if plot_dynamic_over_static_pressure == 1
figure
plot(time_vec,dynamic_over_static_pressure_vec, 'Color',[200/255,55/255,170/255])
hold on
grid

ax = gca ;

ax.FontSize = 16 ;
